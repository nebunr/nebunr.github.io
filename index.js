//JavaScript for index

// Modal Image Gallery
function onClick(element) {
    document.getElementById("img01").src = element.src;
    document.getElementById("modal01").style.display = "block";
    var captionText = document.getElementById("caption");
    captionText.innerHTML = element.alt;
}

// Change style of navbar on scroll
window.onscroll = function() {scrollFunc()};
function scrollFunc() {
    var navbar = document.getElementById("myNavbar");
    if (document.body.scrollTop > 100 || document.documentElement.scrollTop > 100) {
        navbar.className = "w3-bar" + " w3-card" + " w3-animate-top" + " w3-white" + " w3-text-dark-grey";
    } else {
        navbar.className = navbar.className.replace(" w3-card w3-animate-top w3-white", "");
    }
}

// Used to toggle the menu on small screens when clicking on the menu button (hamburger menu)
function hamburgerToggle() {
    var x = document.getElementById("navDemo");
    var y = document.getElementById("myNavbar");
    if (x.className.indexOf("w3-show") == -1) {
        x.className += " w3-show";
        y.className += " w3-white";
    } else {
        x.className = x.className.replace(" w3-show", "");
        y.className = y.className.replace(" w3-white", "");
    }
}

//jQuery Functions
$(document).ready(function() {
    //Smooth scrolling when using clicking to a #id
    $('a[href^="#"]').on('click', function(event) {
        var target = $(this.getAttribute('href'));
        if(target.length) {
            event.preventDefault();
            $('html, body').stop().animate({
                scrollTop: target.offset().top
            }, 800);
        }
    });
});